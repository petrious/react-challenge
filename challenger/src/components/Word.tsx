function Words() {
    const text = "Breaking Bad";
    const textSplited = text.split(" ");

    const matchWords = textSplited.map(word => {
        const twoLetters = word.slice(0,2);

        const isMatchedElement = periodicTable.includes(twoLetters.toLowerCase());
        if(isMatchedElement){
           return <span><span className="WordMatch">{word.substr(0, 2)}</span>{word.substr(2)}</span>;
        }
        return <span>{word}{' '}</span>;
    })


  return <>{matchWords}</>;
}

